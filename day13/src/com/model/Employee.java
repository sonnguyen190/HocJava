package com.model;

public class Employee {
int id;
String fullName;
String dob;
int paymentPerHour;

public Employee() {
	super();
}

public Employee(int id, String fullName, String dob, int paymentPerHour) {
	super();
	this.id = id;
	this.fullName = fullName;
	this.dob = dob;
	this.paymentPerHour = paymentPerHour;
}



}
